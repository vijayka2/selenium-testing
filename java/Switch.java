package practice;

public class Switch {

	public static void main(String[] args) 
	{
		char a='E';
		switch(a) 
		{
		case 'C':System.out.println("this is C");
		break;
		case 'D':System.out.println("this is D");
		break;
		case 'E':System.out.println("this is E");
		break;
		default:System.out.println("This is not C,D,E");

		
		
		}
  }

}


